#include <stdio.h>
int main() 
{
    char color;
    scanf("%d",&color);
    if(char=='R')
    {
        printf("Stop");
    }
    else if(char=='G')
    {
        printf("Go");
    }
    else if(char=='Y')
    {
        printf("Slow Down");
    }
    else
    {
        printf("Invalid input");
    }
    return 0;
}